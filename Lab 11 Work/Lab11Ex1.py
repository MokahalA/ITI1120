def m(i):
    '''(int) -> (float)
    Returns the sum of the following series 1/3+2/5+...+(i)/(2*i+1) for a given integer i.
    '''
    if i == 1:
        return 1/3
    else:
        return m(i-1)+(i)/(2*i+1)

    

